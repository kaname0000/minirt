#include "vec.h"

//å’Œ
t_vec	vec_add(t_vec v1, t_vec v2)
{
	t_vec	vec;

	vec.x = v1.x + v2.x;
	vec.y = v1.y + v2.y;
	vec.z = v1.z + v2.z;
	return (vec);
}
